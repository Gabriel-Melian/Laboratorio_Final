    private void cargarTareas() {
        ArrayList<Proyecto> proyectos = pd.consultarProyectos();
        
        for (Proyecto aux : proyectos) {
            jComboBox.addItem(aux);
        }
    }
    
    private void llenarTabla() {
        
        borrarFila();
        Proyecto proyectoSelec = (Proyecto) jComboBox.getSelectedItem();
        
        ArrayList<Proyecto> lista = pd.consultarProyectos();
        
        for (Proyecto aux : lista) {
            modelo.addRow(new Object []{aux.getId_Proyecto(),aux.getNombre(),aux.getFechaInicio()});
        }
        
    }
    
    private void borrarFila() {
        
        int filas = modelo.getRowCount() - 1;
        
        for (int i=filas; i>=0; i--) {
            
            modelo.removeRow(i);
            
        }
    }
    
    
    
