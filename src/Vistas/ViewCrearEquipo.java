    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        try {
            String nombre = jtNombre.getText();
            
            SimpleDateFormat formatoFecha = new SimpleDateFormat("dd-MM-yyyy");
            String fecha = formatoFecha.format(jdFechaCreacion.getDate());
            LocalDate fechaC = LocalDate.parse(fecha, DateTimeFormatter.ofPattern("dd-MM-yyyy"));
            
            Proyecto proyecto = (Proyecto) jComboBox.getSelectedItem();
            
            //id_Proyecto, nombre, fechaCreacion, estado.
            Equipo equipo = new Equipo(proyecto.getId_Proyecto(),nombre,fechaC,true);

            EquipoData ed = new EquipoData();

            ed.crearEquipo(equipo);

            if (equipo != null) {
                jtNombre.setText(equipo.getNombre());
            }
            else {
                jtNombre.requestFocus();
            }

        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Error. Campos invalidos o incompletos.");
            jtNombre.requestFocus();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

